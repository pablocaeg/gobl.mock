func generateExamples() error {
	examples := []struct {
		name   string
		regime l10n.TaxCountryCode
		addon  cbc.Key
	}{
		{"es-standard", "ES", ""},
		{"de-xrechnung", "DE", "de-xrechnung-v3"},
		{"fr-standard", "FR", ""},
		{"it-sdi", "IT", "it-sdi-v1"},
		{"mx-cfdi", "MX", "mx-cfdi-v4"},
		{"br-nfe", "BR", "br-nfe-v4"},
		{"pt-saft", "PT", "pt-saft-v1"},
		{"us-standard", "US", ""},
	}

	for _, ex := range examples {
		opts := []mock.Option{
			mock.WithRegime(ex.regime),
			mock.WithSeed(42),
		}
		if ex.addon != "" {
			opts = append(opts, mock.WithAddon(ex.addon))
		}

		env, err := mock.Envelope(opts...)
		if err != nil {
			return fmt.Errorf("%s: %w", ex.name, err)
		}

		data, err := json.MarshalIndent(env, "", "  ")
		if err != nil {
			return fmt.Errorf("%s: marshaling: %w", ex.name, err)
		}

		path := fmt.Sprintf("examples/%s.json", ex.name)
		if err := os.WriteFile(path, append(data, '\n'), 0644); err != nil {
			return fmt.Errorf("%s: writing: %w", ex.name, err)
		}
		fmt.Printf("  %s\n", path)
	}
	return nil
}
