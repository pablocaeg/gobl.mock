func main() {
	if err := generateTestData(); err != nil {
		fmt.Fprintf(os.Stderr, "generating test data: %v\n", err)
		os.Exit(1)
	}
	if err := generateExamples(); err != nil {
		fmt.Fprintf(os.Stderr, "generating examples: %v\n", err)
		os.Exit(1)
	}
	fmt.Println("done")
}

func generateTestData() error {
	regimes := tax.AllRegimeDefs()
	addons := tax.AllAddonDefs()

	codes := make([]string, 0, len(regimes))
	for _, r := range regimes {
		codes = append(codes, string(r.Country))
	}
	sort.Strings(codes)

	type addonEntry struct {
		Regime string
		Key    string
	}
	entries := make([]addonEntry, 0, len(addons))
	for _, ad := range addons {
		regime, ok := addonRegime[ad.Key]
		if !ok {
			// Default: derive country from first 2 chars of addon key.
			prefix := strings.ToUpper(string(ad.Key[:2]))
			regime = l10n.TaxCountryCode(prefix)
		}
		entries = append(entries, addonEntry{
			Regime: string(regime),
			Key:    string(ad.Key),
		})
	}

	tmpl := template.Must(template.New("").Parse(testDataTmpl))
	f, err := os.Create("generated_test.go")
	if err != nil {
		return err
	}
	defer f.Close()

	return tmpl.Execute(f, struct {
		Regimes []string
		Addons  []addonEntry
	}{
		Regimes: codes,
		Addons:  entries,
	})
}

